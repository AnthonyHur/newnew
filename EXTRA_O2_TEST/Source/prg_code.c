Module_Status Program_Main() 
{
	HWND			hDlg = NULL;
	int				nParameterLengh , nLoop_Count;
	char*			pcParameter = NULL;
	char			cSet_Data[MAX_PARAMETER_DATA][MAX_PARAMETER_LENGTH] = {0,};
	char			cSeparate_Data[DEFAULT_STRING] = {0,} , cSeparate_Tail[DEFAULT_STRING] = {0,};
	Module_Status	msResult;
	int				nPORTID, nIndex, nSlotIdx;
	int				nStart_Slot = 0 , nEnd_Slot = 0 ,nCntRcp = 0 ;
	char			cPJOBID[MAX_SLOT][DEFAULT_STRING] = {0,}, cPJOBCREATE[MAX_SLOT][DEFAULT_STRING] = {0,}, cTotalPJOBID[DEFAULT_STRING] = {0,};
	char			cCJOBID[DEFAULT_STRING] = {0,}, cCJOBCREATE[MID_STR_LEN] = {0,}, cMIDNAME[DEFAULT_STRING] = {0,}, cHOTRUN[DEFAULT_STRING] = {0,};
	char			cRunMessage[DEFAULT_STRING] = {0,}, cSchMessage[DEFAULT_STRING] = {0,}, cErrorMessage[DEFAULT_STRING] = {0,};
	char			cReadRecipe[DEFAULT_STRING] = {0,}, cBeforeRecipe[DEFAULT_STRING] = {0,};
	int				nCommStatus;
	char			*pcSTRATSTR[4] = { "STARTP(F)" , "STARTP2(F)" , "STARTP3(F)" , "STARTP4(F)" };

	//------------------------------------------------------------------
	// Initialize
	//------------------------------------------------------------------
	ZeroMemory( cSet_Data , sizeof(cSet_Data) );
	ZeroMemory( cSeparate_Data , sizeof(cSeparate_Data) );	ZeroMemory( cSeparate_Tail , sizeof(cSeparate_Tail) );
	msResult = SYS_SUCCESS;

	pcParameter = PROGRAM_PARAMETER_READ();
	nParameterLengh = strlen( pcParameter );
	pcParameter[ nParameterLengh ] = 0x00;	

	hDlg = GetForegroundWindow();
	
	LogMsgWrite( "FUNCTION" , "[START] [EXTRA_O2_TEST] %s" , pcParameter );

	if ( nParameterLengh <= 0 )
	{
		sprintf( cErrorMessage , "Function parameter is null" );
		MessageBox(hDlg, cErrorMessage, "ERROR", 0);
		//MODAL_MESSAGE_BOX( hDlg , "ERROR" , cErrorMessage , "OK" , 0 , &nDlgItem );
		LogMsgWrite( "FUNCTION" , "[ END ] [EXTRA_O2_TEST] %s" , cErrorMessage );
		return SYS_ABORTED;
	}

	//------------------------------------------------------------------
	// Parameter Data Separate
	//	
	//	cSet_Data[0]	cSet_Data[1]
	//	1 or 2 or 3		STARTPx(F) or STARTPEx(F)
	//------------------------------------------------------------------
	for ( nLoop_Count = 0 ; nLoop_Count < MAX_PARAMETER_DATA ; nLoop_Count++ )
	{
		STR_SEPERATE( pcParameter , cSeparate_Data , cSeparate_Tail , DEFAULT_STRING );
		strcpy( cSet_Data[nLoop_Count] , cSeparate_Data );
		
		if ( strlen( cSeparate_Tail ) == 0 )	break;
		pcParameter = cSeparate_Tail;
	}
	nPORTID = atoi( cSet_Data[0] ) - 1;
	strcpy( cRunMessage , cSet_Data[1] );

	//	ERROR CHECK
	//////////////////////////////////////////////////////////////////////////
	if		( strcmp( cRunMessage , pcSTRATSTR[nPORTID] ) )
		sprintf( cErrorMessage , "PORT%d argument(\"%s\") is invalid" , nPORTID + 1 , cRunMessage );
	else if ((READ_DIGITAL( D_CTC_MAIN_CONTROL + nPORTID , &nCommStatus ) != Idle) &&
			 ((READ_DIGITAL( D_CTC_MAIN_CONTROL + nPORTID , &nCommStatus ) != Disable) || 
			  (READ_DIGITAL( D_CTC_PM1M_CONTROL , &nCommStatus ) != 1)))
		sprintf( cErrorMessage , "PORT%d status is not idle" , nPORTID + 1 );
	else if ( Confirm_Wafer_In_PM( nPORTID ) == SYS_ABORTED )
		sprintf( cErrorMessage , "[WARNING] Please make sure there are no wafers in the PM!" );

	if (strlen(cErrorMessage) > 0)
	{
		MessageBox(hDlg, cErrorMessage, "ERROR", 0);
		//MODAL_MESSAGE_BOX( hDlg , "ERROR" , cErrorMessage , "OK" , 0 , &nDlgItem );
		LogMsgWrite("FUNCTION", "[ END ] [EXTRA_O2_TEST] %s", cErrorMessage);
		return SYS_ABORTED;
	}

	sprintf( cSchMessage , "MAPPING_F CM%d" , nPORTID + 1 );
	WRITE_STRING( S_SCHEDULER , cSchMessage , &nCommStatus );
	_sleep( 100 );
	sprintf( cSchMessage , "CONTROLJOB MAPVERIFY|CM%d" , nPORTID + 1 );
	WRITE_STRING( S_SCHEDULER , cSchMessage , &nCommStatus );
	_sleep( 100 );

	READ_STRING( S_CTC_MID_READ + nPORTID	 , cMIDNAME , &nCommStatus ); // Carrier ID
	if ( strlen( cMIDNAME ) <= 0 )
	{
		if		( nPORTID == 0 )	strcpy( cMIDNAME , " " );
		else if	( nPORTID == 1 )	strcpy( cMIDNAME , "  " );
		else if	( nPORTID == 2 )	strcpy( cMIDNAME , "   " );
		else						strcpy( cMIDNAME , "    " );
		WRITE_STRING( S_CTC_MID_READ + nPORTID	, cMIDNAME , &nCommStatus );
	}

	READ_STRING( S_CTC_JID_READ + nPORTID	, cCJOBID	, &nCommStatus ); // Control Job Name = Process Job Name
	if ( strlen( cCJOBID ) <= 0 )	sprintf( cCJOBID , "$M_CJ%d$" , nPORTID + 1 ); // CJ name

	//Initialize Before Recipe
	sprintf(cReadRecipe , "" );
	for ( nIndex = 0, nSlotIdx = 0; nIndex <= MAX_SLOT; nIndex++ )
	{
		strcpy(cBeforeRecipe , cReadRecipe);
	//---------------------------------
	//a.Reading Selected Cluster Recipe
		if		(nPORTID == 0)	READ_STRING( S_CTC_CLUSTER01_RECIPE + nIndex , cReadRecipe , &nCommStatus );
		else if	(nPORTID == 1)	READ_STRING( S_CTC_CLUSTER01_RECIPE2 + nIndex , cReadRecipe , &nCommStatus );
		else if	(nPORTID == 2)	READ_STRING( S_CTC_CLUSTER01_RECIPE3 + nIndex , cReadRecipe , &nCommStatus );
		printf("Selected Cluster Recipe:: [%s]\n",cReadRecipe);

	//---------------------------------
	//b.Check Same Recipe & Counting
	//c.Create P-Job (Process Job & start End Slot)
	//Check Same RCP or End Slot	
		if ((strcmp(cBeforeRecipe, cReadRecipe) || (nIndex == MAX_SLOT)))
		{
			//Count End Slot
			nEnd_Slot += nCntRcp;
			
			// Before Recipe is existence
			if ( strlen(cBeforeRecipe) > 0 )
			{
				sprintf( cPJOBID[nSlotIdx] , "%s[%d]_%d" , "$PJOB$",nPORTID,nSlotIdx ); // PJ Name
				//	  PJOB : "JOBCONTROL PRJOB_CREATE2|PJNAME|CARRIERID|STARTSLOT|ENDSLOT|PPID|PJSTARTMODE "
				sprintf( cPJOBCREATE[nSlotIdx] ,  "JOBCONTROL PRJOB_CREATE2|%s|%s|%d|%d|%s|%d|0" ,cPJOBID[nSlotIdx], cMIDNAME, nStart_Slot, nEnd_Slot, cBeforeRecipe, g_nJobStartMode ); 
				printf( "\t\tPJOB::{%s}\n", cPJOBCREATE[nSlotIdx] );
				WRITE_STRING( S_GEM_CONTROL , cPJOBCREATE[nSlotIdx] , &nCommStatus );
				//Count Created P-Job
				nSlotIdx++;
			}
			//Count Start Slot
			nStart_Slot = nEnd_Slot + 1;
			nCntRcp = 0;
			Sleep(500);
		}
		nCntRcp++;
	}
	if (nSlotIdx == 0)
	{
		sprintf( cErrorMessage , "[WARNING] No slots selected" );
		MessageBox(hDlg, cErrorMessage, "ERROR", 0);
		LogMsgWrite("FUNCTION", "[ END ] [EXTRA_O2_TEST] %s", cErrorMessage);
		return SYS_ABORTED;
	}

	//--------------
	//d.Create C-Job
	strcpy( cTotalPJOBID , "" );
	for( nIndex = 0 ; nIndex < nSlotIdx ; nIndex++ )	
	{
		strcat(cTotalPJOBID , ":");
		strcat(cTotalPJOBID , cPJOBID[nIndex]);
	}
	
	sprintf( cCJOBCREATE , "JOBCONTROL CTJOB_CREATE|%s|%d|%d%s", cCJOBID, g_nJobStartMode, nSlotIdx, cTotalPJOBID);
	WRITE_STRING( S_GEM_CONTROL , cCJOBCREATE , &nCommStatus );
	printf("CJOB is [%s]\n", cCJOBCREATE);

	if      (nPORTID == 0)	WRITE_DIGITAL(D_O2Test_Run_Sts,  Running, &nCommStatus);
	else if (nPORTID == 1)	WRITE_DIGITAL(D_O2Test_Run_Sts2, Running, &nCommStatus);
	else if (nPORTID == 2)	WRITE_DIGITAL(D_O2Test_Run_Sts3, Running, &nCommStatus);

	WRITE_DIGITAL(D_O2Level_Monitoring, MNTR_START, &nCommStatus);
	WRITE_DIGITAL(D_O2LevelCheck_Save, 1, &nCommStatus);
	LogMsgWrite( "FUNCTION" , "[ END ] [EXTRA_O2_TEST] %s (%d)" , pcParameter , SYS_SUCCESS );

	return SYS_SUCCESS;
}
